const firstName = 'John';
const lastName = 'Doe';
const age = 30;
// Example of embedding variables
const greeting = `Hello, ${firstName} ${lastName}!`;
console.log(greeting); // Output: Hello, John Doe!
// Example of embedding an expression
const summary = `Hello, ${firstName} ${lastName}! You are ${age} years old.`;
console.log(summary); // Output: Hello, John Doe! You are 30 years old.


